package com.example.assassin;

public class Text {
	public int id;
	public String title;
	public String content;
	public double time;
	public int sortid;
	
	public Text(int id, String title, String content, double time, int sortid){
		this.id = id;
		this.title = title;
		this.content = content;
		this.time = time;
		this.sortid = sortid;
	}

}
